    private void limpiarControles() {
        txtCodigo.setText("");
        txtAmbiente.setText("");
        txtHoraFin.setText("");
        txtHoraInicio.setText("");
        txtCodigo.requestFocus();
        dateFecha.setDateFormatString("");
        chkEstado.setSelected(false);
        
    }
