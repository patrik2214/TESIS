    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            Character estado;
            if (btnNuevo.getText().equals("Nuevo")) {
                limpiarControles();
                txtCodigo.setText(String.valueOf(clsHorario.generarCodigo()));
                btnNuevo.setText("Guardar");
            } else {
                btnNuevo.setText("Nuevo");
                clsHorario objHorario = new clsHorario();
                objHorario.setId(Integer.parseInt(txtCodigo.getText()));
                objHorario.setFecha((java.sql.Date)dateFecha.getDate());
                objHorario.setHora_inicio(Time.valueOf(txtHoraInicio.getText()));
                objHorario.setHora_fin(Time.valueOf(txtHoraFin.getText()));
                objHorario.setEstado(chkEstado.isSelected());
                objHorario.Registrar();
                limpiarControles();
                JOptionPane.showMessageDialog(this, "Ambiente registrado correctamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar Ambiente");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

