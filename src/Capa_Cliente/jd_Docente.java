    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
                      
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                limpiar();              
                txtDNI.requestFocus();
                btnNuevo.setText("Guardar");
            } else {
                btnNuevo.setText("Nuevo");
                clsDocente objD = new clsDocente();
                objD.setId(clsDocente.generarCodigo());
                objD.setNombre(txtNombre.getText());
                objD.setApellido(txtApellido.getText());
                objD.setDni(txtDNI.getText());     
                objD.setFecha_nacimiento(new java.sql.Date(dcFechaNacimiento.getDate().getTime()));                     
                objD.setDireccion(txtDireccion.getText());                
                objD.setCorreo(txtCorreo.getText());           
                if(cboEstado.getSelectedItem().toString().equalsIgnoreCase("Activo")){
                    objD.setEstado("A");
                }else{
                    objD.setEstado("I");
                }   
                objD.registrar();
                limpiar();
                JOptionPane.showMessageDialog(this, "Docente registrado correctamente");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar Categoria");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        // TODO add your handling code here:
               
        try {
            
            clsDocente objD = new clsDocente();
            objD.setDni(txtDNI.getText());
            objD.setNombre(txtNombre.getText());
            objD.setApellido(txtApellido.getText());
            objD.setFecha_nacimiento(new java.sql.Date(dcFechaNacimiento.getDate().getTime()));
            objD.setDireccion(txtDireccion.getText());
            objD.setCorreo(txtCorreo.getText());
            if (cboEstado.getSelectedItem().toString().equalsIgnoreCase("Activo")){
                objD.setEstado("A");
            }else{
                objD.setEstado("B");
            }
            objD.modificar(objD);          
            limpiar();
            JOptionPane.showMessageDialog(this, "Docente modificado correctamente");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al modificar Docente");
        }
    }//GEN-LAST:event_btn_modificarActionPerformed

