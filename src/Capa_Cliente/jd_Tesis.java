    public void registrarTesis() {
        try {
            if (btnGuardar.getText().equals("Nuevo")) {
                limpiar2();
                txtTitulo.requestFocus();
                btnGuardar.setText("Guardar");
            } else if(btnGuardar.getText().equals("Guardar")) {
                if (!txtTitulo.getText().equalsIgnoreCase("")){
                    btnGuardar.setText("Nuevo");
                    clsTesis objT = new clsTesis();
                    objT.setId(clsTesis.generarCodigo());
                    objT.setTitulo(txtTitulo.getText());
                    objT.setTema(txtTema.getText());
                    objT.setFecha(clsJDBC.fecha_actual());
                    objT.setNota(Integer.parseInt(txtNota.getText()));
                    objT.setResolucionJurado(txtResolucion_Jurado.getText());
                    objT.setFecha_jurado(new java.sql.Date(dcFecha_Jurado.getDate().getTime()));
                    objT.setResolucionAsesor(txtResolucion_Asesor.getText());
                    objT.setFecha_asesor(new java.sql.Date(dcFecha_Asesor.getDate().getTime()));
                    objT.setResolucionSustentacion(txtResolucion_Sustentacion.getText());
                    objT.setFecha_sustentacion(new java.sql.Date(dcFecha_Sustentacion.getDate().getTime()));
                    objT.setUrl("url");// subir archivo
                    objT.setEstado(chkEstado.isSelected());
                    objT.setAlumno(clsAlumno.buscar(alumno_seleccionado));
                    objT.Registrar();
                    limpiar();
                    btnModificar.setEnabled(true);
                    btnSustentacion.setEnabled(true);
                    JOptionPane.showMessageDialog(this, "Tesis agregada correctamente");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar Tesis");
        }
    }
