    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        try {
            if (!txt_nombre.getText().equals("")) {
                clsAmbiente objA = new clsAmbiente();
                ArrayList<clsAmbiente> busqueda = objA.buscar(txt_nombre.getText());
                if (busqueda.size() > 0) {
                    DefaultTableModel modelo = new DefaultTableModel();
                    modelo.addColumn("Código");
                    modelo.addColumn("Nombre");
                    modelo.addColumn("Descripción");
                    modelo.addColumn("Estado");
                    tbl_ambiente.setModel(modelo);

                    busqueda.forEach(a -> {
                        modelo.addRow(new Object[]{
                            a.getId(),
                            a.getNombre(),
                            a.getDescripcion(),
                            a.getEstado()});
                    });
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró ambiente");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese un nombre válido");
                txt_nombre.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar Ambiente");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_nuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_nuevoActionPerformed
        try {
            Character estado;
            if (btn_nuevo.getText().equals("Nuevo")) {
                limpiarControles();
                txt_codigo.setText(String.valueOf(clsAmbiente.generarCodigo()));
                txt_nombre.requestFocus();
                btn_nuevo.setText("Guardar");
            } else {
                btn_nuevo.setText("Nuevo");
                clsAmbiente objA = new clsAmbiente();
                objA.setId(Integer.parseInt(txt_codigo.getText()));
                objA.setNombre(txt_nombre.getText());
                objA.setDescripcion(txt_descripcion.getText());
                if (cbo_estado.getSelectedItem().equals("Activo")) {
                    estado = 'A';
                } else {
                    estado = 'I';
                }
                objA.setEstado(Character.toString(estado));
                objA.registrar();
                limpiarControles();
                JOptionPane.showMessageDialog(this, "Ambiente registrado correctamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar Ambiente");
        }
    }//GEN-LAST:event_btn_nuevoActionPerformed

