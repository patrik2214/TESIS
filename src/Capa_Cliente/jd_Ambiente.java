    private void tbl_ambienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_ambienteMouseClicked
        txt_codigo.setText(String.valueOf(tbl_ambiente.getValueAt(tbl_ambiente.getSelectedRow(), 0)));
        llenarCampos();
    }//GEN-LAST:event_tbl_ambienteMouseClicked
    private void limpiarControles() {
        txt_codigo.setText("");
        txt_nombre.setText("");
        txt_descripcion.setText("");
        cbo_estado.setSelectedIndex(-1);
        txt_codigo.requestFocus();
        limpiarTabla(tbl_ambiente);
    }

    private void llenarCampos() {
        try {
            String estado;
            clsAmbiente objA = clsAmbiente.buscar(Integer.parseInt(txt_codigo.getText()));
            txt_codigo.setText(String.valueOf(objA.getId()));
            txt_nombre.setText(objA.getNombre());
            txt_descripcion.setText(objA.getDescripcion());
            if (objA.getEstado().equals("A")) {
                estado = "Activo";
            } else {
                estado = "Inactivo";
            }
            cbo_estado.setSelectedItem(estado);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al buscar Ambiente");
        }
    }
    public void limpiarTabla(JTable tabla){
        try {
            DefaultTableModel modelo=(DefaultTableModel) tabla.getModel();
            int filas=tabla.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla");
        }
    }
